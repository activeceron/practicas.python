# Operadores matematicos

# Variables
numero_uno = 10
numero_dos = 4

# Operaciones matematicas
print("Operaciones matematicas en Python3")

#Retorna la SUMA en un entero
print("'+' Retorna la SUMA en un entero")

resultado = numero_uno + numero_dos

print("Suma: ", resultado)
print("Tipo de dato: ", type(resultado))

#Retorna la RESTA en un entero
print("'-' Retorna la RESTA en un entero")

resultado = numero_uno - numero_dos

print("Resta: ", resultado)
print("Tipo de dato: ", type(resultado))

#Retorna la MULTIPLICACION en un entero
print("'*' Retorna la MULTIPLICACION en un entero")

resultado = numero_uno * numero_dos

print("Multiplicacion: ", resultado)
print("Tipo de dato: ", type(resultado))

#Retorna la DIVISION en un flotante
print("'/' Retorna la DIVISION en un flotante")

resultado = numero_uno / numero_dos

print("Division: ", resultado)
print("Tipo de dato: ", type(resultado))

#Retorna la DIVISION en un entero
print("'//' Retorna la DIVISION en un entero")

resultado = numero_uno // numero_dos

print("Division: ", resultado)
print("Tipo de dato: ", type(resultado))

#Retorna el EXPONENCIAL de un numero en un entero
print("'**' Retorna el EXPONENCIAL de un numero en un entero")

resultado = numero_uno ** numero_dos

print("Exponencial: ", resultado)
print("Tipo de dato: ", type(resultado))
